    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'routename' => 'required',
            'description'  => 'required',
        ]);

        // dd(json_encode($request->get('points')));

        $newRoute = new Route;
        $newRoute->name = $request->routename;
        $newRoute->UserId = auth()->user()->id;
        $newRoute->points = json_encode($request->get('points'));
        $newRoute->description = $request->description;
        $newRoute->save();

        return redirect('/');

    }

